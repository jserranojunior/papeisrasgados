        <p>To display the social profiles with counter, you can either use [aps-counter-pro] <strong>Shortcode</strong> or you can use <strong>AccessPress Social Counter Pro Widget</strong> from Appearance's widget section or the floating sidebar.</p>
        <p>For the complete documentation please visit:<br/> <a href="https://accesspressthemes.com/documentation/documentationplugin-instruction-accesspress-social-pro/" target="_blank">https://accesspressthemes.com/documentation/documentationplugin-instruction-accesspress-social-pro/</a></p>
        <h3>Shortcode Parameters</h3>
        <p>
            You can pass theme parameter to get the desired theme.By default, the theme that is configured in the display settings will be shown.
            theme="theme-1",theme="theme-2" .. to theme-20
        
        </p>
        <p>For example: [aps-counter-pro theme="theme-19"]</p>
        
        <p><strong>Other Parameters are :</strong></p>
        <ul>
            <li>animation="animation-1" to animation-5</li>
            <li>profiles="facebook,twitter,instagram,googlePlus,soundcloud,dribbble,youtube,steam,vimeo,pinterest,forrst,vk,flickr,behance,github,envato,posts,comments"</li>
            <li>counter_format="default" or short or comma</li>
        </ul>
        
        
